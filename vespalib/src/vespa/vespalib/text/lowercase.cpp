std::string
LowerCase::convert(std::string_view input)
{
    std::string output;
    Utf8Reader r(input);
    Utf8Writer w(output);

    while (r.hasMore()) {
        uint32_t lower = r.getChar();
        uint32_t upper = convert(lower);
        w.putChar(upper);
    }
    return output;
}

std::vector<uint32_t>
LowerCase::convert_to_ucs4(std::string_view input)
{
    std::vector<uint32_t> result;
    result.reserve(input.size());
    Utf8Reader reader(input.data());
    while (reader.hasMore()) {
        result.emplace_back(convert(reader.getChar()));
    }
    return result;
}


